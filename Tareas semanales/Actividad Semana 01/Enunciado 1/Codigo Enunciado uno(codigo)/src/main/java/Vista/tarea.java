    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
         try { 
        float precio = Float.parseFloat(txtPrecio.getText().trim()); 
        int cantidad = Integer.parseInt(txtCantidad.getText().trim()); 
 
        if (precio > 0 && cantidad > 0) { 
            float importeCompra = precio * cantidad; 
            float desc1 = 0.07f * importeCompra; 
            float desc2 = 0.07f * (importeCompra - desc1); 
            float descTotal = desc1 + desc2; 
            float importePagar = importeCompra - descTotal; 
 
            this.txtSalida.setText("\tResultados"); 
            this.txtSalida.append("\n-----------------------------"); 
            this.txtSalida.append("\nImporte compra: " + importeCompra); 
            this.txtSalida.append("\nPrimer descuento (7%): " + desc1); 
            this.txtSalida.append("\nSegundo descuento (7%): " + desc2); 
            this.txtSalida.append("\nDescuento total: " + descTotal); 
            this.txtSalida.append("\nImporte a pagar: " + importePagar); 
 
        } else { 
            JOptionPane.showMessageDialog(this, "Ingrese valores mayores a CERO"); 
            this.txtPrecio.requestFocus(); 
        } 
    } catch (Exception e) { 
        JOptionPane.showMessageDialog(this, "Datos inv√°lidos, intente nuevamente."); 
    } 
    }//GEN-LAST:event_btnCalcularActionPerformed

