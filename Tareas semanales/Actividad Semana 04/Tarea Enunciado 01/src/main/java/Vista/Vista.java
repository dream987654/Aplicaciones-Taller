     public void dibujarFigura(String figura, int lado) {
        this.panelDibujo.setVisible(true);

        Graphics g = this.panelDibujo.getGraphics();
        g.clearRect(0, 0, panelDibujo.getWidth(), panelDibujo.getHeight());

        int x = 50, y = 50;

        switch (figura) {
            case "Cuadrado":
                g.drawRect(x, y, lado, lado);
                break;
            case "Tri√°ngulo":
                g.drawPolygon(
                    new int[]{x, x + lado / 2, x + lado},
                    new int[]{y + lado, y, y + lado},
                    3
                );
                break;
            case "Pent√°gono":
                PanelDibujo pent = new PanelDibujo(5, x + lado/2, y + lado/2, lado/2);
                g.drawPolygon(pent.getXPoints(), pent.getYPoints(), pent.getNumeroLados());
                break;

            case "Hex√°gono":
                PanelDibujo hex = new PanelDibujo(6, x + lado/2, y + lado/2, lado/2);
                 g.drawPolygon(hex.getXPoints(), hex.getYPoints(), hex.getNumeroLados());
                break;
        }
    }
